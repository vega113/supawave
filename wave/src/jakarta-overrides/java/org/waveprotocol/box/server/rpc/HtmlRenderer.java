  /**
   * Appends the Google Analytics tracking script if an account is configured.
   *
   * @param sb      the builder to append to
   * @param account the GA account ID (may be null or empty)
   * @param error   optional custom-variable error string (may be null)
   */
  private static void appendAnalyticsFragment(StringBuilder sb, String account, String error) {
    if (account == null || account.isEmpty()) {
      return;
    }
    sb.append("<script type=\"text/javascript\">\n");
    sb.append("var _gaq = _gaq || [];\n");
    sb.append("_gaq.push(['_setAccount', ").append(escapeJsonString(account)).append("]);\n");
    if (error != null && !error.isEmpty()) {
      sb.append("_gaq.push(['_setCustomVar', 1, 'error', ").append(escapeJsonString(error)).append(", 3]);\n");
    }
    sb.append("_gaq.push(['_trackPageview']);\n");
    sb.append("(function() {\n");
    sb.append("  var ga = document.createElement('script'); ga.type = 'text/javascript'; ga.async = true;\n");
    sb.append("  ga.src = ('https:' == document.location.protocol ? 'https://ssl' : 'http://www') + '.google-analytics.com/ga.js';\n");
    sb.append("  var s = document.getElementsByTagName('script')[0]; s.parentNode.insertBefore(ga, s);\n");
    sb.append("})();\n");
    sb.append("</script>\n");
  }

