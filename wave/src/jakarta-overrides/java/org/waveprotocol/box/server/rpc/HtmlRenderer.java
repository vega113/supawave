  /**
   * Appends the Google Analytics tracking script if an account is configured.
   *
   * @param sb      the builder to append to
   * @param account the GA account ID (may be null or empty)
   * @param error   optional custom-variable error string (may be null)
   */
  private static void appendAnalyticsFragment(StringBuilder sb, String account, String error) {
    if (account == null || account.isEmpty()) {
      return;
    }
    sb.append("<script type=\"text/javascript\">\n");
    sb.append("var _gaq = _gaq || [];\n");
    sb.append("_gaq.push(['_setAccount', ").append(escapeJsonString(account)).append("]);\n");
    if (error != null && !error.isEmpty()) {
      sb.append("_gaq.push(['_setCustomVar', 1, 'error', ").append(escapeJsonString(error)).append(", 3]);\n");
    }
    sb.append("_gaq.push(['_trackPageview']);\n");
    sb.append("(function() {\n");
    sb.append("  var ga = document.createElement('script'); ga.type = 'text/javascript'; ga.async = true;\n");
    sb.append("  ga.src = ('https:' == document.location.protocol ? 'https://ssl' : 'http://www') + '.google-analytics.com/ga.js';\n");
    sb.append("  var s = document.getElementsByTagName('script')[0]; s.parentNode.insertBefore(ga, s);\n");
    sb.append("})();\n");
    sb.append("</script>\n");
  }

  // =========================================================================
  // HTML / JSON escaping helpers
  // =========================================================================

  /**
   * Escapes a string for safe inclusion in HTML content or attribute values.
   * Handles the five XML special characters: {@code & < > " '}.
   *
   * @param s the raw string (may be null)
   * @return the escaped string, or empty string if null
   */
  public static String escapeHtml(String s) {
    if (s == null) return "";
    StringBuilder sb = new StringBuilder(s.length() + 16);
    for (int i = 0; i < s.length(); i++) {
      char c = s.charAt(i);
      switch (c) {
        case '&':  sb.append("&amp;");  break;
        case '<':  sb.append("&lt;");   break;
        case '>':  sb.append("&gt;");   break;
        case '"':  sb.append("&quot;"); break;
        case '\'': sb.append("&#39;");  break;
        default:   sb.append(c);
      }
    }
    return sb.toString();
  }

  /**
   * Wraps a string as a JSON string literal (with surrounding quotes), escaping
   * special characters. Returns {@code "null"} (unquoted) for null input.
   *
   * @param s the raw string (may be null)
   * @return a JSON string literal, e.g. {@code "hello \"world\""}
   */
  public static String escapeJsonString(String s) {
    if (s == null) return "null";
    StringBuilder sb = new StringBuilder(s.length() + 16);
    sb.append('"');
    for (int i = 0; i < s.length(); i++) {
      char c = s.charAt(i);
      switch (c) {
        case '"':  sb.append("\\\""); break;
        case '\\': sb.append("\\\\"); break;
        case '\n': sb.append("\\n");  break;
        case '\r': sb.append("\\r");  break;
        case '\t': sb.append("\\t");  break;
        case '/':  sb.append("\\/");  break;
        default:
          if (c < 0x20) {
            sb.append(String.format("\\u%04x", (int) c));
          } else {
            sb.append(c);
          }
      }
    }
    sb.append('"');
    return sb.toString();
  }
